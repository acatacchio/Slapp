import 'package:flutter/material.dart';

class ColorTheme {

  Color background() => const Color.fromRGBO(35, 35, 45, 1);
  Color text() => const Color.fromRGBO(255, 255, 255, 1);
  Color textGrey() => const Color.fromRGBO(140, 140, 150, 1);
  Color orange() => const Color.fromRGBO(222, 128, 40, 1);
  Color blue() => const Color.fromRGBO(70, 65, 210, 1);
  Color pink() => const Color.fromRGBO(175, 50, 165, 1);
  Color card() => const Color.fromRGBO(45, 45, 55, 1);
}